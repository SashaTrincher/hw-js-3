let str = 'Hello World!';

console.log(`The length of the string is ${str.length}`);
console.log(`The string in all lowercase is ${str.toLowerCase()}`);
console.log(`The string in all uppercase is ${str.toUpperCase()}`);
console.log(`The index of the letter 'o' in the string is ${str.indexOf('o')}`);
console.log(`Does the string include the word 'World'? ${str.includes('World')}`);
